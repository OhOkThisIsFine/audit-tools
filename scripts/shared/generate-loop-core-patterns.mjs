#!/usr/bin/env node
// Regenerate `.claude/hooks/loop-core-patterns.mjs` from the canonical
// TypeScript source of truth (`src/shared/loopCorePaths.ts`).
//
// WHY THIS EXISTS. The loop-core path list had THREE homes: the TS module plus a
// hand-maintained copy inside each of the two hooks (`pre-commit-gate.mjs`,
// `attest-loop-core-review.mjs`). The hooks run as `PreToolUse` under plain node
// BEFORE any build, so they genuinely cannot import the TS module — but that
// argues for ONE generated sibling they both import, not two hand-kept copies.
// A parity test held the invariant, so nothing ever landed broken; the cost was
// the discovery path. Adding `src/audit/cli/dispatchAttempted.ts` meant editing
// two copies and finding the third only when the parity test went red. The
// property that should hold: ONE edit adds a loop-core path.
//
// This is the project's standing "never hand-maintain a table something else can
// generate" rule applied to our own tree.
//
//   node scripts/shared/generate-loop-core-patterns.mjs           # write
//   node scripts/shared/generate-loop-core-patterns.mjs --check   # verify only
//
// `--check` is wired into verify:checks so a stale generated file fails the
// build rather than silently narrowing the gate.
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), "..", "..");
const sourcePath = join(repoRoot, "src", "shared", "loopCorePaths.ts");
const outputPath = join(repoRoot, ".claude", "hooks", "loop-core-patterns.mjs");

/** Pull the string entries out of the canonical `LOOP_CORE_PATTERNS` literal. */
export function extractPatterns(tsSource) {
  const match = tsSource.match(/export const LOOP_CORE_PATTERNS[^=]*=\s*\[([\s\S]*?)\];/);
  if (!match) {
    throw new Error(`could not find the LOOP_CORE_PATTERNS array literal in ${sourcePath}`);
  }
  const patterns = [...match[1].matchAll(/"([^"]+)"/g)].map((m) => m[1]);
  if (patterns.length === 0) {
    throw new Error("LOOP_CORE_PATTERNS parsed as empty — refusing to generate an empty gate");
  }
  return patterns;
}

export function renderModule(patterns) {
  return (
    `// @generated by scripts/shared/generate-loop-core-patterns.mjs — DO NOT EDIT.\n` +
    `// Source of truth: src/shared/loopCorePaths.ts\n` +
    `//\n` +
    `// The .claude hooks run under plain node before any build, so they cannot\n` +
    `// import the TypeScript module. They import THIS generated sibling instead,\n` +
    `// so the list has exactly one hand-maintained home. Run the generator (or\n` +
    `// \`npm run check:loop-core-patterns\`) after editing the source.\n` +
    `export const LOOP_CORE_PATTERNS = [\n` +
    patterns.map((p) => `  ${JSON.stringify(p)},\n`).join("") +
    `];\n`
  );
}

function main() {
  const patterns = extractPatterns(readFileSync(sourcePath, "utf8"));
  const rendered = renderModule(patterns);

  if (process.argv.includes("--check")) {
    let current = null;
    try {
      current = readFileSync(outputPath, "utf8");
    } catch {
      /* missing */
    }
    if (current !== rendered) {
      process.stderr.write(
        `\n${outputPath} is stale or missing.\n` +
          `The loop-core gate would run against a DIFFERENT path list than the source of truth, ` +
          `which silently narrows or widens which commits require review attestation.\n` +
          `Fix: node scripts/shared/generate-loop-core-patterns.mjs\n\n`,
      );
      process.exit(1);
    }
    process.stdout.write(
      `✓ loop-core-patterns: generated hook list matches ${patterns.length} canonical pattern(s)\n`,
    );
    process.exit(0);
  }

  writeFileSync(outputPath, rendered, "utf8");
  process.stdout.write(`wrote ${outputPath} (${patterns.length} patterns)\n`);
}

// Importable as a library (the parity test exercises `renderModule` directly),
// so the CLI body runs ONLY on direct invocation — importing this module must
// never write to the tree as a side effect.
const invokedDirectly =
  process.argv[1] !== undefined && import.meta.url === pathToFileURL(process.argv[1]).href;

if (invokedDirectly) main();
